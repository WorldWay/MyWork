package com.sqkj.znyj.model;

public class CFYP {
	private long id;
	private String ZJQIP;
	private int PMDZ;
	private String YPBH;
	private int YPSL;
	private int FYCS;
	
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getZJQIP() {
		return ZJQIP;
	}

	public void setZJQIP(String zJQIP) {
		ZJQIP = zJQIP;
	}

	public int getPMDZ() {
		return PMDZ;
	}

	public void setPMDZ(int pMDZ) {
		PMDZ = pMDZ;
	}

	public String getYPBH() {
		return YPBH;
	}

	public void setYPBH(String yPBH) {
		YPBH = yPBH;
	}

	public int getYPSL() {
		return YPSL;
	}

	public void setYPSL(int yPSL) {
		YPSL = yPSL;
	}

	public int getFYCS() {
		return FYCS;
	}

	public void setFYCS(int fYCS) {
		FYCS = fYCS;
	}
	
}

package com.sqkj.znyj.model;

public class YPXX {

	private String YPBH;
	private String YPMC;
	private String XQ;
	private String FL;
	private int YPSL;
	
	public YPXX(){}

	public String getYPBH() {
		return YPBH;
	}

	public void setYPBH(String yPBH) {
		YPBH = yPBH;
	}

	public String getYPMC() {
		return YPMC;
	}

	public void setYPMC(String yPMC) {
		YPMC = yPMC;
	}

	public String getXQ() {
		return XQ;
	}

	public void setXQ(String xQ) {
		XQ = xQ;
	}

	public String getFL() {
		return FL;
	}

	public void setFL(String fL) {
		FL = fL;
	}

	public int getYPSL() {
		return YPSL;
	}

	public void setYPSL(int yPSL) {
		YPSL = yPSL;
	}
	
}
